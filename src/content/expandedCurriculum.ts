import type { LearningModule, Lesson, LessonSkillTag, LocalizedSupport } from "@/src/types/content";

type SupportPair = {
  hiDeva: string;
  hiLatn: string;
};

type SentenceSpec = SupportPair & {
  text: string;
  notes?: SupportPair;
};

type FillSpec = SupportPair & {
  start: string;
  end: string;
  answer: string;
  options: string[];
};

type ChoiceSpec = SupportPair & {
  answer: string;
  wrong: [string, string];
};

type FixSpec = SupportPair & {
  incorrect: string;
  answer: string;
  wrong: string;
};

type ArrangeSpec = SupportPair & {
  words: string[];
};

type ExpandedLessonSpec = {
  id: string;
  moduleId: string;
  title: string;
  durationMinutes: number;
  overview: string;
  skipOverview: string[];
  primary: SentenceSpec;
  secondary: SentenceSpec;
  meaningDistractors: [string, string];
  fill: FillSpec;
  choice: ChoiceSpec;
  fix: FixSpec;
  arrange: ArrangeSpec;
  speak: SentenceSpec;
  tags: LessonSkillTag[];
};

function localized({ hiDeva, hiLatn }: SupportPair): LocalizedSupport {
  return {
    "hi-Deva": hiDeva,
    "hi-Latn": hiLatn,
  };
}

function createSentence(lessonId: string, suffix: string, sentence: SentenceSpec) {
  return {
    id: `${lessonId}-${suffix}`,
    targetLanguage: "en-IN" as const,
    targetText: sentence.text,
    support: localized(sentence),
    ...(sentence.notes ? { notes: localized(sentence.notes) } : {}),
  };
}

function createExpandedLesson(spec: ExpandedLessonSpec): Lesson {
  const primarySentence = createSentence(spec.id, "primary", spec.primary);
  const secondarySentence = createSentence(spec.id, "secondary", spec.secondary);
  const speakSentence = createSentence(spec.id, "speak", spec.speak);

  return {
    id: spec.id,
    moduleId: spec.moduleId,
    title: spec.title,
    durationMinutes: spec.durationMinutes,
    overview: spec.overview,
    skipOverview: spec.skipOverview,
    activities: [
      {
        type: "sentence",
        prompt: "Study the main sentence for this situation.",
        sentence: primarySentence,
      },
      {
        type: "sentence",
        prompt: "Add one supporting sentence to make your meaning clearer.",
        sentence: secondarySentence,
      },
      {
        type: "chooseMeaning",
        prompt: "Choose the meaning of the main sentence.",
        sentence: primarySentence,
        options: [spec.primary.hiDeva, ...spec.meaningDistractors],
        answer: spec.primary.hiDeva,
        explanation: localized({
          hiDeva: "यह option पूरे English sentence का सही अर्थ बताता है।",
          hiLatn: "Yeh option poore English sentence ka sahi meaning batata hai.",
        }),
      },
      {
        type: "fillBlank",
        prompt: "Choose the phrase that completes the sentence naturally.",
        sentenceStart: spec.fill.start,
        sentenceEnd: spec.fill.end,
        options: spec.fill.options,
        answer: spec.fill.answer,
        explanation: localized(spec.fill),
      },
      {
        type: "choice",
        prompt: "Which sentence sounds natural in this situation?",
        options: [spec.choice.answer, ...spec.choice.wrong],
        answer: spec.choice.answer,
        explanation: localized(spec.choice),
      },
      {
        type: "fixSentence",
        prompt: "Fix the sentence so it sounds clear and respectful.",
        incorrectSentence: spec.fix.incorrect,
        options: [spec.fix.answer, spec.fix.incorrect, spec.fix.wrong],
        answer: spec.fix.answer,
        explanation: localized(spec.fix),
      },
      {
        type: "arrangeWords",
        prompt: "Arrange the words into a clear spoken sentence.",
        words: spec.arrange.words,
        answer: spec.arrange.words,
        explanation: localized(spec.arrange),
      },
      {
        type: "speak",
        prompt: "Say the full sentence with a calm, confident voice.",
        sentence: speakSentence,
      },
    ],
  };
}

